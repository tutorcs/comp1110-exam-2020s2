    @Test
    public void testHashCodeDeterministic() {
        checkNotObjectHashCode();
        for (Q5Actor actor : actors) {
            int hash = actor.hashCode();
            for (int i = 0; i < 10; i++) {
                int hash2 = actor.hashCode();
                assertTrue("actor " + actor + "  returned different values for hashCode(): " + hash + ", " + hash2, hash == hash2);
            }
        }
    }

